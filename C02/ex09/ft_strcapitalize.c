char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (i == 0 || (str[i - 1] >= ' ' && str[i - 1] <= '/') ||
				(str[i - 1] >= ':' && str[i - 1] <= '@') ||
				(str[i - 1] >= '[' && str[i - 1] <= '`') ||
				(str[i - 1] >= '{' && str[i - 1] <= '~'))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] -= 32;
		}
		i++;
	}
	return (str);
}
