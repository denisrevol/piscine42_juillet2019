void	ft_rev_int_tab(int *tab, int size)
{
	int n;
	int i;
	int tmp;

	n = size;
	i = 0;
	while (i < (n / 2))
	{
		tmp = tab[n - i];
		tab[n - i] = tab[i];
		tab[i] = tmp;
		i++;
	}
}
